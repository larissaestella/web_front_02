/* É uma estrutura de decisão que não trabalha com condições “verdadeiros e falsos”, mas sim com valores 
diretos */

let teste = 4;

switch (teste) {
    case 1:
        console.log("foi na primeira")
        break;
    case 2:
        console.log("foi na segunda")
        break;
    default:
        console.log("não foi dessa vez")
}